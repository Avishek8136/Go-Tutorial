package main

func main() {
	//bitwise operators
	println(5 | 3)     //bitwise OR
	println(5 & 3)     //bitwise AND
	println(5 ^ 3)     //bitwise XOR
	println(5 &^ 3)    //bit clear
	println(5000 >> 3) //right shift
	println(5000 << 3) //left shift
}

